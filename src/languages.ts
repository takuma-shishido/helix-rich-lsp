const regexLangList: [RegExp, string][] = Object.entries(RegexLangMap).map(([regex, lang]) => [new RegExp(regex), lang])
const extLangList: [string, string][] = Object.entries(ExtLangMap)

export function getLanguage(filename: string): string | null {
  for (const [regex, lang] of regexLangList) {
    if (regex.exec(filename)) return lang
  }

  for (const [ext, lang] of extLangList) {
    if (filename.endsWith(ext)) return lang
  }

  return null
}
